#include "3-calc.h"

/**
 * op_add - addition operation
 * @a: first number
 * @b: second number
 *
 * Return: (@a + @b)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction operation
 * @a: first number
 * @b: second number
 *
 * Return: (@a + @b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication operation
 * @a: first number
 * @b: second number
 *
 * Return: (@a + @b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division operation
 * @a: first number
 * @b: second number
 *
 * Return: (@a + @b)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- modulo operation
 * @a: first number
 * @b: second number
 *
 * Return: (@a + @b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}

#include <stdio.h>
/**
 * main -  program that prints the number of arguments passed into it.
 * @argc: the number of command-line arguments
 * @argv: the array of strings that contains these arguments.
 * Return: 0
 */
int main(int argc, char *argv[])
{
	(void)argc;
	printf("%d\n", argc (-1));
	return (0);
}
